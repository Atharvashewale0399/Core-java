package com.atharva.arrays;

public class DataTypesArray {

	public static void main(String[] args) {
		int [] marks = {47, 74, 93, 83, 89};
		System.out.println(marks[2]);
		float [] marks1 = {76.3f, 95.2f, 69.7f, 82.6f };//--> FLOATING VALUE ARRAY
		System.out.println(marks1[1]);
		
		//                                      TO FIND ARRAY LENGHT
	System.out.println(marks.length);
		//                                      STRING ARRAY
		String [] names = {"atharva", "priya", "chirag", "bhushan"};
		System.out.println(names[2]);
	
		
	}
}
